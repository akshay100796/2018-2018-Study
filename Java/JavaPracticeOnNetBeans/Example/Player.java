package Example;

public class Player 
{
    int age;
    String name;
    double wt;
    double ht;

    public Player(int age, String name, double wt, double ht) {
        this.age = age;
        this.name = name;
        this.wt = wt;
        this.ht = ht;
    }

    public int getAge() {
        return age;
    }

    public String getName() {
        return name;
    }

    public double getWt() {
        return wt;
    }

    public double getHt() {
        return ht;
    }
    
    
    
}
